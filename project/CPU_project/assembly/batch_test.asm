# =============================================================================
# batch_test.asm - RV32I 基础功能测试汇编程序
# 所有基础用例写在同一个文件中
# =============================================================================
# I/O 地址（MMIO）:
#   拨码开关读取: 0xFFFFFC60 (低16位), 0xFFFFFC62 (高8位)
#   LED 写入:     0xFFFFFC60 (低16位), 0xFFFFFC62 (高8位)
#
# 寄存器约定:
#   x0  = 0 (hardwired)
#   x1  = ra (return address)
#   x2  = sp (stack pointer)
#   x10 = a0 (函数参数/返回值)
#   x11 = a1 (函数参数)
#   x5-x7 = t0-t2 (临时寄存器)
#   x28-x31 = t3-t6 (临时寄存器)
# =============================================================================

# --- 初始化 I/O 基地址 ---
# lui x31, 0xFFFFF      =>  x31 = 0xFFFFF000
# addi x31, x31, 0x460  =>  x31 = 0xFFFFFC60  (I/O 基地址，偏移 0xC60 不对)
# 
# 更正：lui 加载高20位，addi 加载低12位
# 0xFFFFFC60 = 0xFFFFF000 + 0xC60
# 但 addi 是符号扩展的，0xC60 = 0b110001100000
# 如果 imm = 0xC60 = 3168，在12位有符号范围内(-2048~2047)，超出了
# 所以需要拆分：lui x31, 0x100000 => x31 = 0x00000000 (因为取高20位)
#
# 正确方法：
# lui   x31, 0xFFFFF     =>  x31 = 0xFFFFF000
# ori   x31, x31, 0xC60  =>  x31 = 0xFFFFFC60
# 但 ori 立即数也是12位符号扩展...
#
# 最简单方法（不考虑正负）：
# lui   x30, 0x10000      =>  x30 = 0x10000000  (用于 lui+add 测试)
#
# 实际上对于 0xFFFFFC60:
# lui   x31, 0x00000     =>  x31 = 0x00000000
# 因为 0xFFFFF 的低位会被 addi 的符号扩展影响
# 正确分解: 0xFFFFFC60
#   高20位 = 0x00000 (如果 bit11=1，需要+1)
#   低12位 = 0xC60 = -0x3A0 的补码... 
#   bit11 = 1, 所以 lui 值 = 0xFFFFF + 1 = 0x100000 → 溢出为 0x00000
#
# 这里用更实用的方式：
# 假设 I/O 地址为 0xFFFF_FC60
# lui  x31, 0x100000  会变成 0x00000000（溢出）
#
# 直接用简化方案：I/O 地址 = 0xFFFFFC00 区域
# lui   x31, 0xFFFFF     =>  x31 = 0xFFFFF000  (高20位)
# addi  x30, x0, -928    =>  x30 = 0xFFFFFC60  (0xFFFFFC60 = -0x3A0 = -928? 不对)
#
# 实用方案：直接硬编码地址计算
#   lui   x31, 0x10000    这不对...
#
# 最终方案（参考课程实现）:
# I/O 地址判断条件是 alu_result[31:10] 全为1
# 即地址 >= 0xFFFFFC00
# 我们用地址 0xFFFFFC60 读 switch, 0xFFFFFC60 写 LED
# 构造方法:
#   lui  x31, 0xFFFFF    =>  x31 = 0xFFFFF000 (因为 lui 加载 imm<<12)
#                             0xFFFFF << 12 = 0xFFFFF000
#   addi x31, x31, 0x460 =>  不行，0x460=1120 在12位范围内? [-2048,2047] 是的!
#                             但 0xFFFFF000 + 0x460 = 0xFFFFF460 ≠ 0xFFFFFC60
# 
# 让我直接算: 0xFFFFFC60
#   高20位 (需要考虑 addi 符号扩展):
#   低12位 = 0xC60 = 3168, 但12位有符号最大2047, 所以 0xC60 被解释为负数
#   0xC60 = 0b1100_0110_0000, 符号位=1, 值 = -(0x400 - 0x60) = -(1024-96) 不对
#   12位补码: 0xC60 = -0x3A0 = -928
#   所以 addi x31, x31, -928 即 addi x31, x31, 0xC60
#   但这意味着 lui 部分需要是 0xFFFFFC60 - (-928) = 0xFFFFFC60 + 928 = 0x100000588
#   不对...
#
#   标准做法:
#   目标 = upper + sign_ext(lower)
#   lower = 0xC60, 因为 bit11=1, sign_ext = 0xFFFFF_C60
#   所以 upper = target - sign_ext(lower) = 0xFFFFFC60 - 0xFFFFFC60 = 0
#   即: lui x31, 0x00000; addi x31, x31, 0xC60  => 0 + 0xFFFFFC60 = 0xFFFFFC60? 
#   不! addi 的立即数符号扩展到32位: 0xC60 的12位表示是 -928
#   lui x31, 0 => x31 = 0
#   addi x31, x31, -928 => x31 = -928 = 0xFFFFFC60  ✓ 对了!

# =============================================================================
# 程序入口 (地址 0x00000000)
# =============================================================================
_start:
    # 构造 I/O 基地址 0xFFFFFC60 到 x31
    addi  x31, x0, -928       # x31 = 0xFFFFFC60 (switch/LED 地址)
    addi  x30, x0, -926       # x30 = 0xFFFFFC62 (switch/LED 高8位地址)

# =============================================================================
# 用例0: AND 运算
# 从 switch 低16位读取操作数A（低8位）和操作数B（高8位）
# 计算 A & B, 结果输出到 LED
# =============================================================================
test0_and:
    lw    x5, 0(x31)           # x5 = switch[15:0]
    andi  x6, x5, 0xFF        # x6 = 操作数A = x5[7:0]
    srli  x7, x5, 8           # x7 = x5 >> 8
    andi  x7, x7, 0xFF        # x7 = 操作数B = x5[15:8]
    and   x10, x6, x7         # x10 = A & B
    sw    x10, 0(x31)         # 输出到 LED

# =============================================================================
# 用例1: SLL 逻辑左移
# 操作数: switch[7:0], 移位量: switch[12:8] (低5bit)
# =============================================================================
test1_sll:
    lw    x5, 0(x31)           # x5 = switch[15:0]
    andi  x6, x5, 0xFF        # x6 = 操作数 = x5[7:0]
    srli  x7, x5, 8           # x7 = x5 >> 8
    andi  x7, x7, 0x1F        # x7 = 移位量 (低5位)
    sll   x10, x6, x7         # x10 = x6 << x7
    sw    x10, 0(x31)         # 输出到 LED

# =============================================================================
# 用例2: SRA 算术右移
# 操作数: switch[7:0] (作为有符号8位数，符号扩展到32位)
# 移位量: switch[12:8] (低5bit)
# =============================================================================
test2_sra:
    lw    x5, 0(x31)           # x5 = switch[15:0]
    andi  x6, x5, 0xFF        # x6 = 操作数 (8位)
    # 将8位数符号扩展到32位
    slli  x6, x6, 24          # 左移24位
    srai  x6, x6, 24          # 算术右移24位 -> 符号扩展完成
    srli  x7, x5, 8
    andi  x7, x7, 0x1F        # x7 = 移位量
    sra   x10, x6, x7         # x10 = x6 >>> x7 (算术右移)
    sw    x10, 0(x31)         # 输出到 LED

# =============================================================================
# 用例3: LUI + ADD
# 演示 lui 加载高位立即数，然后 add 合并
# =============================================================================
test3_lui_add:
    lw    x5, 0(x31)           # x5 = switch 输入值
    lui   x6, 0x12345          # x6 = 0x12345000
    add   x10, x6, x5         # x10 = 0x12345000 + switch_value
    sw    x10, 0(x31)         # 输出到 LED

# =============================================================================
# 用例4: JAL + AUIPC
# 演示 auipc 获取 PC 相对地址，jal 跳转
# =============================================================================
test4_jal_auipc:
    auipc x6, 0               # x6 = 当前 PC
    jal   x1, test4_target     # 跳转到 target, x1 = PC+4
test4_ret:
    sw    x10, 0(x31)         # 输出结果到 LED
    jal   x0, test5_jal_jalr   # 跳到下一个用例
test4_target:
    add   x10, x6, x1         # x10 = auipc值 + 返回地址
    jal   x0, test4_ret        # 跳回

# =============================================================================
# 用例5: JAL + JALR
# 演示函数调用: jal 跳转保存返回地址, jalr 返回
# =============================================================================
test5_jal_jalr:
    lw    x10, 0(x31)         # x10 = switch 输入
    jal   x1, test5_func       # 调用函数, x1 = 返回地址
    sw    x10, 0(x31)         # 输出结果到 LED
    jal   x0, test6_fibonacci  # 跳到下一个用例
test5_func:
    addi  x10, x10, 1         # x10 = x10 + 1
    jalr  x0, x1, 0           # 返回 (跳转到 x1 的地址)

# =============================================================================
# 用例6: 斐波那契数列
# 输入: n = switch[7:0] (数列下标, 0-indexed)
# 输出: fib(n)
# fib(0)=0, fib(1)=1, fib(2)=1, fib(3)=2, ...
# =============================================================================
test6_fibonacci:
    lw    x5, 0(x31)           # x5 = switch
    andi  x10, x5, 0xFF       # x10 = n (下标)
    addi  x6, x0, 0           # x6 = a = fib(0) = 0
    addi  x7, x0, 1           # x7 = b = fib(1) = 1
    beq   x10, x0, fib_done   # if n==0, return a=0
    addi  x11, x0, 1
    beq   x10, x11, fib_one   # if n==1, return b=1
    addi  x10, x10, -1        # 循环次数 = n-1
fib_loop:
    add   x28, x6, x7         # temp = a + b
    add   x6, x7, x0          # a = b
    add   x7, x28, x0         # b = temp
    addi  x10, x10, -1        # n--
    bne   x10, x0, fib_loop   # if n != 0, continue
fib_one:
    add   x6, x7, x0          # a = b (最终结果在 x7)
fib_done:
    sw    x6, 0(x31)           # 输出 fib(n) 到 LED

# =============================================================================
# 用例7: Popcount (统计8bit中1的个数)
# 输入: switch[7:0]
# 输出: 1的个数
# =============================================================================
test7_popcount:
    lw    x5, 0(x31)           # x5 = switch
    andi  x5, x5, 0xFF        # x5 = 8bit 输入数据
    addi  x6, x0, 0           # x6 = count = 0
    addi  x7, x0, 8           # x7 = 循环计数器 = 8
pop_loop:
    andi  x28, x5, 1          # x28 = x5 & 1 (最低位)
    add   x6, x6, x28         # count += 最低位
    srli  x5, x5, 1           # x5 >>= 1
    addi  x7, x7, -1          # 计数器--
    bne   x7, x0, pop_loop    # if 计数器 != 0, continue
    sw    x6, 0(x31)           # 输出 count 到 LED

# =============================================================================
# 用例8: IEEE754 浮点数类型判断 (16bit half-precision)
# 格式: 1bit符号 + 5bit指数 + 10bit尾数
# 输入: switch[15:0]
# 输出编码: 0=±0, 1=±∞, 2=NaN, 3=规约化数, 4=非规约化数
# =============================================================================
test8_fp_classify:
    lw    x5, 0(x31)           # x5 = 16bit 浮点数
    # 提取指数 (bit[14:10])
    srli  x6, x5, 10          # x6 = x5 >> 10
    andi  x6, x6, 0x1F        # x6 = 指数 (5bit, 0~31)
    # 提取尾数 (bit[9:0])
    andi  x7, x5, 0x3FF       # x7 = 尾数 (10bit)

    # 判断逻辑:
    # 指数==0 && 尾数==0  → 0 (±0)
    # 指数==31 && 尾数==0 → 1 (±∞)
    # 指数==31 && 尾数!=0 → 2 (NaN)
    # 指数!=0 && 指数!=31 → 3 (规约化数)
    # 指数==0 && 尾数!=0  → 4 (非规约化数)

    addi  x28, x0, 31         # x28 = 31 (全1指数)

    # 检查指数是否为0
    bne   x6, x0, fp_exp_nonzero
    # 指数 == 0
    beq   x7, x0, fp_is_zero      # 尾数也==0 → ±0
    addi  x10, x0, 4              # 尾数!=0 → 非规约化数 (4)
    jal   x0, fp_done
fp_is_zero:
    addi  x10, x0, 0              # ±0 (0)
    jal   x0, fp_done

fp_exp_nonzero:
    # 指数 != 0, 检查是否全1
    bne   x6, x28, fp_is_normal
    # 指数 == 31
    beq   x7, x0, fp_is_inf       # 尾数==0 → ±∞
    addi  x10, x0, 2              # 尾数!=0 → NaN (2)
    jal   x0, fp_done
fp_is_inf:
    addi  x10, x0, 1              # ±∞ (1)
    jal   x0, fp_done

fp_is_normal:
    addi  x10, x0, 3              # 规约化数 (3)

fp_done:
    sw    x10, 0(x31)             # 输出类型编码到 LED

# =============================================================================
# 用例9: 浮点数量化 (16bit float → Q3.4 定点数)
# Q3.4格式: 1bit符号 + 3bit整数 + 4bit小数 (共8bit)
# 输入: switch[15:0] (16bit half-precision float)
# 输出: Q3.4 定点数 (负数输出补码)
# =============================================================================
test9_fp_quantize:
    lw    x5, 0(x31)           # x5 = 16bit 浮点数
    # 提取符号位 (bit15)
    srli  x10, x5, 15         # x10 = 符号位
    andi  x10, x10, 1
    # 提取指数 (bit[14:10])
    srli  x6, x5, 10
    andi  x6, x6, 0x1F        # x6 = 偏移指数 (bias=15)
    # 提取尾数 (bit[9:0])
    andi  x7, x5, 0x3FF       # x7 = 尾数

    # 检查特殊值：指数==0 或 指数==31 → 输出0
    beq   x6, x0, quant_zero
    addi  x28, x0, 31
    beq   x6, x28, quant_zero

    # 规约化数: 值 = (-1)^s * 2^(exp-15) * 1.mantissa
    # 还原真实指数
    addi  x6, x6, -15         # x6 = 真实指数 (可能为负)

    # 构造定点数表示: 1.mantissa 的整数部分在 bit10, 尾数在 bit[9:0]
    # 即: 完整值 = (0x400 | mantissa) = 1.mantissa * 2^10
    ori   x7, x7, 0x400       # x7 = 1.mantissa * 1024 (Q10.10格式的整数表示)

    # 需要转换到 Q3.4 格式
    # Q3.4 中小数点在 bit4 位置, 所以 1.0 = 0x10 = 16
    # 从 Q10 (尾数已经是 *1024) 转到 Q4 (需要 *16)
    # 即: result = x7 * 2^(exp) / 1024 * 16 = x7 * 2^(exp) / 64
    # 等价于: result = x7 >> (6 - exp)  当 exp < 6
    #         result = x7 << (exp - 6)  当 exp >= 6

    addi  x28, x0, 6          # x28 = 6
    blt   x6, x28, quant_shift_right
    # exp >= 6: 左移
    sub   x29, x6, x28        # shift = exp - 6
    sll   x7, x7, x29         # x7 <<= shift
    jal   x0, quant_clamp
quant_shift_right:
    # exp < 6: 右移
    sub   x29, x28, x6        # shift = 6 - exp
    srl   x7, x7, x29         # x7 >>= shift

quant_clamp:
    # 限幅到 7bit (Q3.4 无符号部分最大 0x7F = 127)
    addi  x28, x0, 127
    blt   x7, x28, quant_no_clamp
    addi  x7, x0, 127         # 饱和到最大值
quant_no_clamp:
    andi  x7, x7, 0xFF        # 截断到8位

    # 处理符号: 如果是负数，取补码
    beq   x10, x0, quant_done # 正数直接输出
    # 负数: 取补码 = ~x7 + 1
    xori  x7, x7, 0xFF        # 按位取反 (8位范围)
    addi  x7, x7, 1           # +1
    andi  x7, x7, 0xFF        # 截断到8位
    jal   x0, quant_done

quant_zero:
    addi  x7, x0, 0           # 特殊值输出0

quant_done:
    sw    x7, 0(x31)           # 输出 Q3.4 定点数到 LED

# =============================================================================
# 程序结束: 死循环
# =============================================================================
end_loop:
    jal   x0, end_loop         # 无限循环
